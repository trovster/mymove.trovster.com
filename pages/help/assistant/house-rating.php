<p>While you are inspecting a house you can rate it in six essential categories, which will sum up
later to a total house quality percentage. Please note that every category should be rated if applicable
and possible. If you don't rate a category you will receive 0 points.</p>

<p class="help-task">By clicking on the drop down menu you can rate a certain category, thereby "crap" is the worst = 0 and "perfect" is the best = 5.</p>

<p class="help-task">After rating all the categories click "okay" in order to return to the property overview page.</p>

<p class="help-task">If you want to remove this certain house from the Assistant, please click "delete" at the bottom.</p>