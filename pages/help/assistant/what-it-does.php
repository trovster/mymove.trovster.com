<p class="help-task">In order to rate a house, please click on the title bar.</p>

<p class="help-task">If you want to remove all properties, please click on "remove all".</p>

<p class="help-task">If you want to remove one certain house, please click on the title bar and thereafter "delete" at the bottom.</p>

<p>If a house is rated, the empty bar will fill and display an exact percentage value. Please notice that the percentage
value is just a rough estimation of the quality of a house. The word quality is defined in terms of lifestyle, cleanness,
safety and environment of the property. In any case a house inspection is required before signing a tenancy contract.</p>